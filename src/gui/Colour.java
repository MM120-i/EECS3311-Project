package gui;

import java.awt.*;

/**
 * Color scheme of the project. All the fields are static so that if functions
 * similarly to how the Color class works.
 */
public class Colour {
	public static Color bg = Color.decode("#072540"); // dark blue
	public static Color lightBg = Color.decode("#183D5D"); // medium blue
	public static Color strike = Color.decode("#93C2D8"); // light blue
	public static Color highlight = Color.decode("#9C4668"); // red
	public static Color strongHighlight = Color.decode("#FF8AE2"); // pink
	public static Color contrast = Color.decode("#FFA500"); // orange
}
